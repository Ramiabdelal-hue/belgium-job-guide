function changeLanguage(lang,btn){
document.documentElement.lang=lang;
document.documentElement.dir=lang==="ar"?"ltr":"ltr";

document.querySelectorAll("[data-i18n]").forEach(el=>{
let key=el.dataset.i18n;
if(translations[lang][key]){
    if(el.tagName==="UL" || el.tagName==="OL"){
        el.innerHTML=translations[lang][key].split("\n").map(i=>"<li>"+i+"</li>").join("");
    } else {
        el.innerText=translations[lang][key];
    }
}
});

document.querySelectorAll(".lang-btn").forEach(b=>b.classList.remove("active"));
btn.classList.add("active");
}
document.addEventListener("DOMContentLoaded", function () {
  const nlBtn = document.querySelector(".lang-btn.active");
  changeLanguage("nl", nlBtn);
});