function changeLang(lang) {
    const data = translations[lang];
    if (!data) return;

    document.documentElement.dir = data.dir;
    document.documentElement.lang = lang;
    // داخل دالة changeLang(lang) أضف هذه الأسطر لتحديث النص برمجياً:
document.getElementById('contact-text').innerText = data.contactText;
document.getElementById('btn-contact-page').innerText = data.contactBtn;

    document.getElementById('btn-back').innerText = data.back;
    document.getElementById('btn-home').innerText = data.home;
    document.getElementById('hero-title').innerText = data.heroTitle;
    document.getElementById('hero-sub').innerText = data.heroSub;
    document.getElementById('card-title').innerText = data.cardTitle;
    document.getElementById('card-desc').innerText = data.cardDesc;
    document.getElementById('soon-note').innerHTML = data.soon;
    document.getElementById('footer-text').innerHTML = data.footer;

    const container = document.getElementById('features-container');
    container.innerHTML = '';
    data.items.forEach(item => {
        container.innerHTML += `
            <div class="feature-item">
                <span class="feature-icon">${item.icon}</span>
                <h3>${item.h}</h3>
                <p>${item.p}</p>
            </div>
        `;
    });

    // تحديث أزرار اللغة
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if(btn.innerText.toLowerCase() === lang) btn.classList.add('active');
    });
}

// التشغيل الافتراضي عند التحميل
window.onload = () => changeLang('nl');
